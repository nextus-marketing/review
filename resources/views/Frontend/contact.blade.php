<script src="https://cdnjs.cloudflare.com/ajax/libs/jquery/1.12.4/jquery.min.js"></script>
<script src="https://cdnjs.cloudflare.com/ajax/libs/toastr.js/latest/toastr.min.js"></script>
<script>
$(document).ready(function () {

    $.ajaxSetup({
        headers: {
            'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
        }
    });

    $('#contactForm').on('submit', function (e) {
        e.preventDefault();

        var form = $(this);
        var submitBtn = $('#submitBtn');
        var btnText = submitBtn.find('.btn-text');

        form.find('div[id$="-error"]').empty();
        var url = form.attr('action');

        $.ajax({
            type: "POST",
            url: url,
            data: new FormData(this),
            contentType: false,
            cache: false,
            processData: false,

            beforeSend: function () {
                submitBtn.prop('disabled', true);
                btnText.text('Submitting...');
                $('#form_loader').show();
            },

            success: function (data) {
                if (data.status === 'success') {
                    toastr.success(data.message, '', {
                        timeOut: 1500,
                        closeButton: true,
                    });

                    form[0].reset();

                    setTimeout(function () {
                        window.location.href = '/thankyou';
                    }, 1000);
                }
            },

            error: function (xhr) {
                toastr.error(
                    'There are some errors in the form. Please check your inputs.',
                    '', {
                        timeOut: 1500,
                        closeButton: true,
                    }
                );

                if (xhr.responseJSON && xhr.responseJSON.errors) {
                    $.each(xhr.responseJSON.errors, function (key, value) {
                        form.find('#' + key + '-error').html(value);
                    });

                    var firstErrorKey = Object.keys(xhr.responseJSON.errors)[0];
                    $('html, body').animate({
                        scrollTop: form.find('#' + firstErrorKey + '-error').offset().top - 200
                    }, 500);
                }
            },

            complete: function () {
                submitBtn.prop('disabled', false);
                btnText.text('Submit Form');
                $('#form_loader').hide();
            }
        });
    });

});
</script>



